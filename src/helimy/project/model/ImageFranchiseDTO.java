package helimy.project.model;

public class ImageFranchiseDTO {
	private Integer imagecode;	// 이미지코드
	private String image;		// 이미지
	private Integer francode;	// 가맹코드
	
	
	public Integer getImagecode() {
		return imagecode;
	}
	public void setImagecode(Integer imagecode) {
		this.imagecode = imagecode;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public Integer getFrancode() {
		return francode;
	}
	public void setFrancode(Integer francode) {
		this.francode = francode;
	}
	
	
}
